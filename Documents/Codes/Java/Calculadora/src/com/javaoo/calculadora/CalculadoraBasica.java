// Define o pacote onde a classe CalculadoraBasica está localizada
package com.javaoo.calculadora;

// Define a classe CalculadoraBasica
public class CalculadoraBasica {

    // Método para realizar a operação de adição entre dois números
    public final double adicao(double x, double y) {
        return (x + y);  // Retorna a soma de x e y
    }

    // Método para realizar a operação de subtração entre dois números
    public final double subtrair(double x, double y) {
        return (x - y);  // Retorna a diferença entre x e y
    }

    // Método para realizar a operação de multiplicação entre dois números
    public final double multiplicar(double x, double y) {
        return (x * y);  // Retorna o produto de x e y
    }

    // Método para realizar a operação de divisão entre dois números
    public final double dividir(double x, double y) {
        return (x / y);  // Retorna o quociente de x por y
    }
}


